export const getInitialState = (key, initialValue) => {
  try {
    const item = window.localStorage.getItem(key);
    return item ? JSON.parse(item) : initialValue;
  } catch (error) {
    console.error('Error cargando localStorage', error);
    return initialValue;
  }
};